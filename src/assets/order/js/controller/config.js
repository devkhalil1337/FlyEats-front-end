MyApp.angular.config(function($routeProvider,$locationProvider) {
//    $locationProvider.hashPrefix('');
    $routeProvider
    .when("/main", {
        templateUrl: "js/views/main.html",
        controller: "IndexPageController"
    }).when("/checkout", {
        templateUrl: "js/views/checkout.html",
        controller: "IndexPageController"
    }).when("/login", {
        templateUrl: "js/views/login.html",
        controller: "IndexPageController"
    }).when("/signup", {
        templateUrl: "js/views/signup.html",
        controller: "IndexPageController"
    }).when("/myorders", {
        templateUrl: "js/views/my-account.html",
        controller: "IndexPageController"
    }).when("/myprofile", {
        templateUrl: "js/views/my-profile.html",
        controller: "IndexPageController"
    }).when("/myaddress", {
        templateUrl: "js/views/my-address.html",
        controller: "IndexPageController"
    }).when("/trackorders", {
        templateUrl: "js/views/track-orders.html",
        controller: "IndexPageController"
    }).when("/addaddress", {
        templateUrl: "js/views/add-address.html",
        controller: "IndexPageController"
    }).when("/editaddress", {
        templateUrl: "js/views/edit-address.html",
        controller: "IndexPageController"
    }).when("/stripe", {
        templateUrl: "js/views/stripeform.html",
        controller: "IndexPageController"
    }).when("/ordercheckout", {
        templateUrl: "js/views/check-out-screen.html",
        controller: "IndexPageController"
    }).when("/checkout-details", {
        templateUrl: "js/views/checkout-details.html",
        controller: "IndexPageController"
    }).when("/home", {
        templateUrl: "js/views/home.html",
        controller: "IndexPageController"
    }).when("/contact-us", {
        templateUrl: "js/views/contact_us.html",
        controller: "IndexPageController"
    }).when("/reset-password", {
        templateUrl: "js/views/forget-password.html",
        controller: "IndexPageController"
    }).when("/", {
        templateUrl: "js/views/main.html",
        controller: "IndexPageController"
    });
    $locationProvider.html5Mode(true);
});     